class Lettercomparison {
public static void main(String[] args) {
char letter1 = 'A';
char letter2 = 'B';
System.out.println("'" + letter1 + "' < '" + letter2 + "': " + (letter1 < letter2));
System.out.println("'" + letter1 + "' <= '" + letter2 + "': " + (letter1 <= letter2));
System.out.println("'" + letter1 + "' > '" + letter2 + "': " + (letter1 > letter2));
System.out.println("'" + letter1 + "' >= '" + letter2 + "': " + (letter1 >= letter2));
System.out.println("'" + letter1 + "' == '" + letter2 + "': " + (letter1 == letter2));
System.out.println("'" + letter1 + "' != '" + letter2 + "': " + (letter1 != letter2));
}
}
